package day2.datatype;

public class DataTpes {
    public static void main(String[] args) {
        System.out.println("learning all data types");

        // use of all 8 primitive data types

        // using byte data type

        byte b = 123;
        System.out.println(b);

        // using integer datatypes

        int i = 45;
        System.out.println(i);

        // using short data types

        short s = 23;
        System.out.println(s);


        // using long data types

         long l= 34265643;
        System.out.println(l);

        // using char datatypes

        char c = 'r';
        System.out.println(c);

        // using float data type

        float f = 41.445f;
        System.out.println(f);

        // double data types

        double d = 1455.5346;

        System.out.println(d);

        // boolean

        boolean AreYouDrunk = true;
        System.out.println(AreYouDrunk);

    }
}
